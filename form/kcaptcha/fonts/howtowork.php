<div id="j"><div id="q"><center><b>Назначение</b></center><br>

Практически на каждом сайте (и коммерческом, и развлекательном) необходима обратная связь с посетителями.<br><br>

Необходимость такого скрипта:
<ul>
<li>Если опубликовать свой e-mail, то он быстро попадаем в базу спамеров и к Вам на ящик начинает идти непрерывный поток рекламы.</li>
<li>Посетителям намного удобнее прислать свое сообщение прямо с Вашего сайта (не совершая дополнительных действий).</li>
</ul>

Эти функции удобно реализовать с помощью данного скрипта, он выводит форму и, при заполнении необходимых полей, отправляет на Ваш e-mail сообщение. В скрипт встроена защита от роботов: перед отправкой письма надо ввести капчу.</div></div><br>

<div id="j"><div id="q"><center><b>Изменения в новой версии PHP Mailer Final</b></center><br>

<ul>
<li>Купирована возможность взлома.</li>
<li>Изменен внешний вид формы. Упрощен внешний вид приходящего письма.</li>
<li>В капче отображаются цифры (раньше были цифры и буквы).</li>
</ul>
</div></div><br>

<div id="j"><div id="q"><center><b>Крайне простая установка и настройки</b></center><br>

