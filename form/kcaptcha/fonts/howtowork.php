<li>Откройте текстовым редактором файл form/config.php.</li>
<li>Присвойте переменной $mailto = ""; свой e-mail.</li>
<b>Скрипт готов к работе</b>.<br>
Протестируйте его работу, перейдя по ссылке http://ваш_сайт.ру/form/form.php
